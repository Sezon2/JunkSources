//*************************************************************************************************************************************************************
//★コンストラクタ関数
//-------------------------------------------------------------------------------------------------------------------------------------------------------------
//【説明】
//クラスの初期化を行なう関数です。
//New演算子でインスタンスを生成しなかった場合は任意のタイミングでは破棄できません。
//*************************************************************************************************************************************************************
DictionaryObject::DictionaryObject(){}
DictionaryObject::DictionaryObject( const BitMap			&$InitVar ){ SetDicObject( $InitVar ); }
DictionaryObject::DictionaryObject( const MovieClip			&$InitVar ){ SetDicObject( $InitVar ); }
DictionaryObject::DictionaryObject( const Shape				&$InitVar ){ SetDicObject( $InitVar ); }
DictionaryObject::DictionaryObject( const DictionaryObject	&$InitVar ){ SetDicObject( $InitVar ); }
//*************************************************************************************************************************************************************


//*************************************************************************************************************************************************************
//★デストラクタ関数
//-------------------------------------------------------------------------------------------------------------------------------------------------------------
//【説明】
//本クラスが破棄されたときに必要な処理を行ないます。
//基本的にメモリ開放等、後始末的な処理を行ないます。
//-------------------------------------------------------------------------------------------------------------------------------------------------------------
//【書式】
//delete objvar;
//*************************************************************************************************************************************************************
DictionaryObject::~DictionaryObject(){
	DeleteAllVariables();//全変数の領域開放
}
